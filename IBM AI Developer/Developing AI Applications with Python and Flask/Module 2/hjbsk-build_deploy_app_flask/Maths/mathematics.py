def summation(a, b):
    """This function returns the sum of two numbers

    Args:
        a (int): First number
        b (int): Second number

    Returns:
        int: Sum of a and b
    """
    return a + b

def subtraction(a, b):
    """This function returns the difference between two numbers

    Args:
        a (int): First number
        b (int): Second number

    Returns:
        int: Difference of a and b
    """
    return a - b

def multiplication(a, b):
    """This function returns the product of two numbers

    Args:
        a (int): First number
        b (int): Second number

    Returns:
        int: Product of a and b
    """
    return a * b

